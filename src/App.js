import React from "react";

// Utility: Weather icon by WMO code
function getWeatherIcon(wmoCode) {
  const icons = new Map([
    [[0], "‚òÄÔ∏è"],
    [[1], "üå§"],
    [[2], "‚õÖÔ∏è"],
    [[3], "‚òÅÔ∏è"],
    [[45, 48], "üå´"],
    [[51, 56, 61, 66, 80], "üå¶"],
    [[53, 55, 63, 65, 57, 67, 81, 82], "üåß"],
    [[71, 73, 75, 77, 85, 86], "üå®"],
    [[95], "üå©"],
    [[96, 99], "‚õà"],
  ]);
  const arr = [...icons.keys()].find((key) => key.includes(wmoCode));
  if (!arr) return "‚ùì";
  return icons.get(arr);
}

// Utility: Format date into short weekday
function formatDay(dateStr) {
  return new Intl.DateTimeFormat("en", {
    weekday: "short",
  }).format(new Date(dateStr));
}

// Main App
class App extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      location: "",
      isLoading: false,
      displayLocation: "",
      countryFlagUrl: "",
      weather: {},
    };
    this.fetchWeather = this.fetchWeather.bind(this);
  }

  async fetchWeather() {
    if (!this.state.location.trim()) return;

    try {
      this.setState({ isLoading: true });

      // 1) Get location data (geocoding)
      const geoRes = await fetch(
        `https://geocoding-api.open-meteo.com/v1/search?name=${this.state.location}`
      );
      const geoData = await geoRes.json();
      console.log(geoData);

      if (!geoData.results) throw new Error("Location not found");

      const { latitude, longitude, timezone, name, country_code } =
        geoData.results.at(0);

      // Set display info
      this.setState({
        displayLocation: name,
        countryFlagUrl: `https://flagcdn.com/w80/${country_code.toLowerCase()}.png`,
      });

      // 2) Get weather data
      const weatherRes = await fetch(
        `https://api.open-meteo.com/v1/forecast?latitude=${latitude}&longitude=${longitude}&timezone=${timezone}&daily=weathercode,temperature_2m_max,temperature_2m_min`
      );
      const weatherData = await weatherRes.json();
      this.setState({ weather: weatherData.daily });
    } catch (err) {
      console.error(err); // fixed typo from console.err to console.error
    } finally {
      this.setState({ isLoading: false });
    }
  }

  render() {
    return (
      <div className="app">
        <h1>Classy Weather</h1>

        <div>
          <input
            type="text"
            placeholder="Search for location..."
            value={this.state.location}
            onChange={(e) => this.setState({ location: e.target.value })}
          />
        </div>

        <button onClick={this.fetchWeather}>Get weather</button>

        {this.state.isLoading && <p className="loader">Loading...</p>}

        {this.state.displayLocation && (
          <h2>
            {this.state.displayLocation}
            {this.state.countryFlagUrl && (
              <img
                src={this.state.countryFlagUrl}
                alt="Country flag"
                style={{
                  width: "30px",
                  verticalAlign: "middle",
                  marginLeft: "8px",
                }}
              />
            )}
          </h2>
        )}

        {this.state.weather.weathercode && (
          <Weather
            weather={this.state.weather}
            location={this.state.displayLocation}
          />
        )}
      </div>
    );
  }
}

export default App;

// Weather Component
class Weather extends React.Component {
  render() {
    const {
      temperature_2m_max: max,
      temperature_2m_min: min,
      time: dates,
      weathercode: codes,
    } = this.props.weather;

    return (
      <div>
        <ul className="weather">
          {dates.map((date, i) => (
            <Day
              date={date}
              max={max.at(i)}
              min={min.at(i)}
              code={codes.at(i)}
              key={date}
              isToday={i === 0}
            />
          ))}
        </ul>
      </div>
    );
  }
}

// Day Component
class Day extends React.Component {
  render() {
    const { date, max, min, code, isToday } = this.props;

    return (
      <li className="day">
        <span>{getWeatherIcon(code)}</span>
        <p>{isToday ? "Today" : formatDay(date)}</p>
        <p>
          {Math.floor(min)}&deg; &mdash; <strong>{Math.ceil(max)}&deg;</strong>
        </p>
      </li>
    );
  }
}
